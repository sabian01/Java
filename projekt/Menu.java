public class Menu {
    //Fields
    private String artikull;
    private int cmimi;

    //Konstruktor
    public Menu (String artikull, int cmimi) {
        this.artikull = artikull;
        this.cmimi = cmimi;
    }

    public String getArtikull() {
        return artikull;
    }

    public int getCmimi() {
        return cmimi;
    }

}
